CREATE OR REPLACE VIEW TEM2
AS
SELECT SUBSTR(ODCODE, 1, 6) AS "MONTHS",
        GOCODE,
        GONAME,
        SUM(COUNT) AS "COUNT",
        SUM(OTQTY) AS "QTY",
        SUM(PRICE) AS "PRICE"
FROM FV2
GROUP BY SUBSTR(ODCODE, 1, 6), GOCODE, GONAME;

SELECT MONTHS,
        MAX(QTY)
FROM TEM2
GROUP BY MONTHS;

SELECT *
FROM TEM2
WHERE (MONTHS, QTY) IN(SELECT MONTHS,
                                MAX(QTY)
                        FROM TEM2
                        GROUP BY MONTHS);

